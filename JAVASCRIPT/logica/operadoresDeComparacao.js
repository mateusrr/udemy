/*
> maior que
>= maior que ou igual a 
< menor que
<= menor que ou igual a 
== igualdade (valor)
=== igualdade estrita (valor e tipo)
!= diferente (valor)
!== diferente estrito (valor e tipo)
*/

const numero1 = 10
const numero2 = 5
console.log('1:', numero1 > numero2)
console.log('2:', numero1 >= numero2)
console.log('3:', numero1 <= numero2)
console.log('4:', numero1 < numero2)
console.log('5:', numero1 == numero2)
console.log('6:', numero1 === numero2)
console.log('7:', numero1 != numero2)
console.log('8:', numero1 !== numero2)
